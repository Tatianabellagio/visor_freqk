#!/usr/bin/env python3
"""
launch_experiment_var.py
========================
Generate variation-aware config files and submit the var pipeline to Slurm.
One config + one pipeline submission is created per (coverage × sv_freq × position) combination.

This mirrors launch_experiment.py but targets the variation-aware pipeline:
  N ecotype haplotypes are drawn from the GrENET VCF, N_SV = round(N × SV_FREQ)
  of them receive the deletion, and all N are pooled at equal fractions via SHORtS.

Usage
-----
  # Single condition
  python scripts/launch_experiment_var.py --sv-type DEL --coverage 50 --sv-freq 0.50 --n-samples 10

  # Sweep over coverages, frequencies and positions in one call
  python scripts/launch_experiment_var.py --sv-type DEL \\
      --coverage 10 20 --sv-freq 0.10 0.30 0.50 \\
      --n-samples 100 --sizes 100bp 500bp 1kb 5kb 10kb \\
      --positions 10000000 15000000

  # Dry run (print configs, do not submit)
  python scripts/launch_experiment_var.py --sv-type DEL --coverage 50 --sv-freq 0.50 \\
      --n-samples 10 --positions 10000000 --dry-run

Required
--------
  --sv-type     DEL (INS not yet supported in var pipeline)
  --coverage    one or more sequencing depths (e.g. --coverage 10 20 50)
  --sv-freq     one or more SV frequencies; fraction of N_SAMPLES haplotypes that carry the SV
                (e.g. --sv-freq 0.10 0.30 0.50)
  --n-samples   number of ecotype haplotypes to draw from the GrENET VCF (integer, e.g. 100)

Optional
--------
  --sizes       one or more SV sizes to include, from:
                  100bp  500bp  1kb  5kb  10kb
                (default: 1kb)
                Example: --sizes 1kb 5kb 10kb
  --positions   one or more 0-based SV start positions on Chr1, in bp
                (default: 10000000)
                Must be multiples of 1 Mb so the pos{N}mb label is exact.
  --error-rate  sequencing error rate (float, default: 0.001)
  --k           k-mer size for freqk (integer, default: 31)
  --dry-run     print configs and commands, do not submit

Position → folder label mapping
--------------------------------
  --positions 10000000  →  POS_LABEL="pos10mb"
  --positions 15000000  →  POS_LABEL="pos15mb"
  --positions 25000000  →  POS_LABEL="pos25mb"
"""

import argparse
import itertools
import os
import subprocess
import sys
from pathlib import Path

# ── Fixed paths (adjust if the project moves) ─────────────────────────────────
WORK  = "/home/tbellagio/scratch/visor_freqk"
FREQK = "/home/tbellagio/scratch/pang/test_freqk/freqk/target/release/freqk"
CHROM = "Chr1"
SV_START_0_DEFAULT = 10_000_000

# ── All supported SV sizes (same as original pipeline) ────────────────────────
ALL_DEL_SIZES = {"100bp": 100, "500bp": 500, "1kb": 1000, "5kb": 5000, "10kb": 10000}
ALL_INS_SIZES = {"100bp": 100, "500bp": 500, "1kb": 1000, "5kb": 5000, "10kb": 10000}


# ── Helpers ────────────────────────────────────────────────────────────────────

def format_error_label(error_rate: float) -> str:
    """Produce the ERR label used in script names.
    0.0   -> '0'
    0.01  -> '01'
    0.001 -> '001'
    """
    if error_rate == 0.0:
        return "0"
    s = f"{error_rate}"
    return s.split(".")[1] if "." in s else s


def pos_label_from_pos(pos: int) -> str:
    """10_000_000 -> 'pos10mb', 25_000_000 -> 'pos25mb'"""
    return f"pos{pos // 1_000_000}mb"


# ── Config generation ──────────────────────────────────────────────────────────

def generate_config(sv_type: str, coverage: int, sv_freq: float, n_samples: int,
                    error_rate: float, k: int, pos: int, pos_label: str,
                    sizes: dict) -> str:
    sv = sv_type.lower()
    size_var = "DEL_SIZES" if sv_type == "DEL" else "INS_SIZES"
    size_lines = "\n".join(f'  ["{name}"]={val}' for name, val in sizes.items())

    anchor_pos = pos - 1  # one base before deletion (VCF POS, 1-based)

    return f"""\
#!/bin/bash
# Auto-generated by launch_experiment_var.py
# SV_TYPE={sv_type}  COVERAGE={coverage}  SV_FREQ={sv_freq}  N_SAMPLES={n_samples}
# ERROR_RATE={error_rate}  K={k}  POS={pos}  POS_LABEL={pos_label}

# Common paths
WORK={WORK}
REF=${{WORK}}/data/reference/Chr1.fa

# GrENET VCF (SNPs only)
VCF_FILE=${{WORK}}/data/reference/greneNet_final_v1.1.recode.vcf.gz
CHROM_MAP=${{WORK}}/data/reference/chrom_map.txt
RENAMED_VCF=${{WORK}}/data/reference/greneNet_final_v1.1.recode.Chr1.vcf.gz

# SV design
SV_TYPE="{sv_type}"
CHROM="{CHROM}"
SV_START_0={pos}      # BED 0-based start
ANCHOR_POS={anchor_pos}       # one base before deletion (VCF POS, 1-based)
POS_LABEL="{pos_label}"

# Ecotype panel design
N_SAMPLES={n_samples}
SV_FREQ={sv_freq}

declare -A {size_var}=(
{size_lines}
)

# Pool-seq design
COVERAGE={coverage}
ERROR_RATE={error_rate}
FREQ=${{SV_FREQ}}

# freqk
K={k}
FREQK={FREQK}

# Directories
HAPS_VAR=${{WORK}}/data/haplotypes_var/{sv}/${{POS_LABEL}}
BEDS=${{WORK}}/data/beds/{sv}/${{POS_LABEL}}
READS_VAR=${{WORK}}/data/reads_var/{sv}/${{POS_LABEL}}
READS=${{READS_VAR}}
VCF_DIR=${{WORK}}/data/vcf/{sv}/${{POS_LABEL}}/var
RESULTS=${{WORK}}/results/{sv}/${{POS_LABEL}}/var
"""


# ── CLI ────────────────────────────────────────────────────────────────────────

def parse_args():
    p = argparse.ArgumentParser(
        description="Generate a variation-aware VISOR-freqk config and submit the Slurm pipeline.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    p.add_argument("--sv-type",    required=True, choices=["DEL"],
                   help="SV type (currently DEL only)")
    p.add_argument("--coverage",   required=True, type=int, nargs="+",
                   help="Sequencing coverage(s) — one or more integers (e.g. --coverage 10 20 50)")
    p.add_argument("--sv-freq",    required=True, type=float, nargs="+",
                   help="SV frequency/ies — fraction of N_SAMPLES haplotypes carrying the SV "
                        "(e.g. --sv-freq 0.10 0.30 0.50)")
    p.add_argument("--n-samples",  required=True, type=int,
                   help="Number of ecotype haplotypes to draw from GrENET VCF (e.g. 10)")
    p.add_argument("--sizes",      nargs="+", default=["1kb"],
                   choices=list(ALL_DEL_SIZES.keys()),
                   help="SV sizes to include (default: 1kb). E.g. --sizes 1kb 5kb 10kb")
    p.add_argument("--error-rate", default=0.001, type=float,
                   help="Sequencing error rate (default: 0.001)")
    p.add_argument("--k",          default=31,    type=int,
                   help="k-mer size for freqk (default: 31)")
    p.add_argument("--positions",  nargs="+", type=int,
                   help="0-based SV start positions on CHROM (default: 10000000)")
    p.add_argument("--dry-run",    action="store_true",
                   help="Print config and command but do not submit")
    return p.parse_args()


def main():
    args = parse_args()

    positions  = args.positions if args.positions else [SV_START_0_DEFAULT]
    coverages  = args.coverage   # already a list
    sv_freqs   = args.sv_freq    # already a list

    # Build size dict in a consistent order from the requested names
    all_sizes = ALL_DEL_SIZES if args.sv_type == "DEL" else ALL_INS_SIZES
    sizes = {name: all_sizes[name] for name in args.sizes}

    scripts_dir = Path(__file__).parent
    config_dir  = scripts_dir / "generated_configs"
    config_dir.mkdir(exist_ok=True)

    err_label = format_error_label(args.error_rate)
    pipeline  = scripts_dir / "run_pipeline_var.sh"

    combinations = list(itertools.product(coverages, sv_freqs, positions))
    total = len(combinations)

    print(f"\nLaunching {total} pipeline run(s):")
    print(f"  Coverages  : {coverages}")
    print(f"  SV freqs   : {sv_freqs}")
    print(f"  Positions  : {positions}")
    print(f"  Sizes      : {list(sizes.keys())}")
    print(f"  N samples  : {args.n_samples}")
    print(f"  Error rate : {args.error_rate}  K={args.k}")
    print()

    for i, (cov, freq, pos) in enumerate(combinations, 1):
        pos_label  = pos_label_from_pos(pos)
        freq_label = int(round(freq * 100))

        config_text = generate_config(
            sv_type    = args.sv_type,
            coverage   = cov,
            sv_freq    = freq,
            n_samples  = args.n_samples,
            error_rate = args.error_rate,
            k          = args.k,
            pos        = pos,
            pos_label  = pos_label,
            sizes      = sizes,
        )

        config_name = (
            f"config_var_{args.sv_type.lower()}"
            f"_n{args.n_samples}"
            f"_f{freq_label}"
            f"_cov{cov}"
            f"_err{err_label}"
            f"_k{args.k}"
            f"_{pos_label}.sh"
        )
        config_path = config_dir / config_name
        config_path.write_text(config_text)
        config_path.chmod(0o644)

        # ── Print summary ──────────────────────────────────────────────────
        print(f"[{i}/{total}] {'─'*54}")
        print(f"  SV type    : {args.sv_type}")
        print(f"  Sizes      : {', '.join(sizes.keys())}")
        print(f"  Coverage   : {cov}x")
        print(f"  SV freq    : {freq}  ({int(round(freq * args.n_samples))}/{args.n_samples} haplotypes)")
        print(f"  N samples  : {args.n_samples}")
        print(f"  Error rate : {args.error_rate}")
        print(f"  K          : {args.k}")
        print(f"  Position   : {pos} ({pos_label})")
        print(f"  Config     : {config_path}")

        # ── Launch pipeline ────────────────────────────────────────────────
        cmd = ["bash", str(pipeline), str(config_path)]
        print(f"  Command    : {' '.join(cmd)}")

        if args.dry_run:
            print("  [dry-run] Not submitting.\n")
        else:
            result = subprocess.run(cmd)
            if result.returncode != 0:
                print(f"  ERROR: pipeline returned {result.returncode}, aborting.")
                sys.exit(result.returncode)
            print()

    if not args.dry_run:
        print(f"\nAll {total} pipeline(s) submitted.")


if __name__ == "__main__":
    main()
