#!/usr/bin/env python3
"""
launch_benchmarks.py
====================
Submit the vg giraffe benchmark (step 05b) against already-simulated reads.

Reads, truth VCFs, and haplotypes produced by run_pipeline_var.sh are reused
verbatim — no VISOR re-simulation.  One SLURM job is submitted per
(coverage × sv_freq × rep × size) combination; each job runs 05b_vg_giraffe.sh
with a generated config file.

Usage
-----
  # All registered reps, one size, one coverage, one freq
  python scripts/launch_benchmarks.py \\
      --sv-type DEL --coverage 50 --sv-freq 0.50 \\
      --n-samples 231 --sizes 1kb

  # Subset of reps
  python scripts/launch_benchmarks.py \\
      --sv-type DEL --coverage 10 20 50 --sv-freq 0.10 0.50 0.90 \\
      --n-samples 231 --sizes 1kb 5kb \\
      --rep-ids rep1 rep2 rep3

  # Dry run — print configs + sbatch commands without submitting
  python scripts/launch_benchmarks.py \\
      --sv-type DEL --coverage 50 --sv-freq 0.50 --n-samples 231 \\
      --rep-ids rep1 --sizes 1kb --dry-run

Skipping combinations
---------------------
By default we only submit combinations that already have a completed freqk
output TSV on disk (so the benchmark has something to compare against). Use
--ignore-freqk to submit every combination regardless.

Arguments
---------
  --sv-type     DEL
  --coverage    one or more sequencing depths
  --sv-freq     one or more SV allele frequencies
  --n-samples   number of ecotype haplotypes the reads were simulated from
  --sizes       SV sizes: 100bp 500bp 1kb 5kb 10kb  (default: 1kb)
  --rep-ids     reps to run (default: every rep in positions_registry.tsv)
  --error-rate  sequencing error rate (default: 0.001)
  --k           k-mer size used by freqk (default: 31) — only affects output path
  --ignore-freqk  submit even if no matching freqk TSV exists on disk
  --stagger-secs  seconds between sbatch submissions (default: 2)
  --dry-run     print configs and sbatch commands, do not submit
"""

import argparse
import csv
import datetime
import itertools
import subprocess
import sys
import time
from pathlib import Path

# ── Fixed paths ───────────────────────────────────────────────────────────────
WORK     = "/home/tbellagio/scratch/visor_freqk"
FREQK    = "/home/tbellagio/scratch/bin/freqk"
FASTA    = Path(WORK) / "data/reference/Chr1.fa"
CHROM    = "Chr1"
REGISTRY = Path(WORK) / "data/positions_registry.tsv"

# ── Supported SV sizes ────────────────────────────────────────────────────────
ALL_DEL_SIZES = {"100bp": 100, "500bp": 500, "1kb": 1000, "5kb": 5000, "10kb": 10000}


def _load_registry() -> dict:
    """Return {rep_id: int(pos_bp)}."""
    if not REGISTRY.exists() or REGISTRY.stat().st_size == 0:
        sys.exit(f"ERROR: positions_registry.tsv not found at {REGISTRY}")
    with open(REGISTRY) as f:
        return {row["rep_id"]: int(row["pos_bp"])
                for row in csv.DictReader(f, delimiter="\t")}


def format_error_label(error_rate: float) -> str:
    if error_rate == 0.0:
        return "0"
    s = f"{error_rate}"
    return s.split(".")[1] if "." in s else s


def freqk_tsv_path(sv_type: str, pos_label: str, cov: int, err_label: str,
                   size: str, n_samples: int, freq_label: int, k: int) -> Path:
    sv = sv_type.lower()
    base = (Path(WORK) / f"results/{sv}/{pos_label}/var/"
            f"cov{cov}_err{err_label}/{size}/n{n_samples}/f{freq_label}/k{k}")
    return base / f"var_del_{size}_n{n_samples}_f{freq_label}_err{err_label}.allele_frequencies.k{k}.tsv"


def generate_config(sv_type: str, coverage: int, sv_freq: float, n_samples: int,
                    error_rate: float, k: int, pos: int, pos_label: str,
                    sizes: dict) -> str:
    """Identical shape to launch_experiment_var.py's config so every step
    script (including 05b_vg_giraffe.sh) can source it unchanged."""
    sv         = sv_type.lower()
    size_var   = "DEL_SIZES"
    size_lines = "\n".join(f'  ["{name}"]={val}' for name, val in sizes.items())
    anchor_pos = pos - 1

    return f"""\
#!/bin/bash
# Auto-generated by launch_benchmarks.py
# SV_TYPE={sv_type}  COVERAGE={coverage}  SV_FREQ={sv_freq}  N_SAMPLES={n_samples}
# ERROR_RATE={error_rate}  K={k}  POS={pos}  POS_LABEL={pos_label}

WORK={WORK}
REF=${{WORK}}/data/reference/Chr1.fa
VCF_FILE=${{WORK}}/data/reference/greneNet_final_v1.1.recode.vcf.gz
CHROM_MAP=${{WORK}}/data/reference/chrom_map.txt
RENAMED_VCF=${{WORK}}/data/reference/greneNet_final_v1.1.recode.Chr1.vcf.gz

SV_TYPE="{sv_type}"
CHROM="{CHROM}"
SV_START_0={pos}
ANCHOR_POS={anchor_pos}
POS_LABEL="{pos_label}"

N_SAMPLES={n_samples}
SV_FREQ={sv_freq}

declare -A {size_var}=(
{size_lines}
)

COVERAGE={coverage}
ERROR_RATE={error_rate}
FREQ=${{SV_FREQ}}

K={k}
FREQK={FREQK}

HAPS_WT=${{WORK}}/data/haplotypes_var/wt/n{n_samples}
HAPS_SV=${{WORK}}/data/haplotypes_var/{sv}/${{POS_LABEL}}
BEDS=${{WORK}}/data/beds/{sv}/${{POS_LABEL}}
READS_VAR=${{WORK}}/data/reads_var/{sv}/${{POS_LABEL}}
READS=${{READS_VAR}}
VCF_DIR=${{WORK}}/data/vcf/{sv}/${{POS_LABEL}}/var
RESULTS=${{WORK}}/results/{sv}/${{POS_LABEL}}/var
"""


def parse_args():
    p = argparse.ArgumentParser(
        description="Submit the vg giraffe benchmark (05b) over existing "
                    "simulated reads.  No VISOR re-run.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    p.add_argument("--sv-type",      required=True, choices=["DEL"])
    p.add_argument("--coverage",     required=True, type=int,   nargs="+")
    p.add_argument("--sv-freq",      required=True, type=float, nargs="+")
    p.add_argument("--n-samples",    required=True, type=int)
    p.add_argument("--sizes",        nargs="+", default=["1kb"],
                   choices=list(ALL_DEL_SIZES.keys()))
    p.add_argument("--rep-ids",      nargs="+",
                   help="reps to run (default: all reps in the registry)")
    p.add_argument("--error-rate",   default=0.001, type=float)
    p.add_argument("--k",            default=31,    type=int)
    p.add_argument("--ignore-freqk", action="store_true",
                   help="submit even if no freqk TSV is on disk")
    p.add_argument("--stagger-secs", default=2,     type=int)
    p.add_argument("--dry-run",      action="store_true")
    return p.parse_args()


def main():
    args = parse_args()

    registry = _load_registry()
    rep_ids  = args.rep_ids or list(registry)
    missing  = [r for r in rep_ids if r not in registry]
    if missing:
        sys.exit(f"ERROR: rep_id(s) not in registry: {', '.join(missing)}")

    sizes    = {name: ALL_DEL_SIZES[name] for name in args.sizes}
    err_lbl  = format_error_label(args.error_rate)

    scripts_dir = Path(__file__).parent
    config_dir  = scripts_dir / "generated_configs"
    config_dir.mkdir(exist_ok=True)
    step_05b    = scripts_dir / "05b_vg_giraffe.sh"
    if not step_05b.exists():
        sys.exit(f"ERROR: {step_05b} not found")

    combos = sorted(
        itertools.product(args.coverage, args.sv_freq, rep_ids),
        key=lambda x: (x[2], x[0], x[1]),
    )

    print(f"\nvg giraffe benchmark launcher")
    print(f"  Reps       : {rep_ids}")
    print(f"  Coverages  : {args.coverage}")
    print(f"  SV freqs   : {args.sv_freq}")
    print(f"  Sizes      : {list(sizes)}")
    print(f"  N samples  : {args.n_samples}")
    print(f"  Error      : {args.error_rate}   K={args.k}")
    print(f"  Total combos (cov × freq × rep): {len(combos)}")
    if not args.ignore_freqk:
        print(f"  Skipping combos without a matching freqk TSV on disk")
    print()

    ts = datetime.datetime.now().strftime("%Y%m%d_%H%M%S")
    jobids_file = Path(WORK) / f"logs/benchmarks_{ts}.jobids"
    jobids_file.parent.mkdir(parents=True, exist_ok=True)

    submitted = 0
    skipped   = 0
    jobids    = []

    for i, (cov, freq, rep_id) in enumerate(combos, 1):
        pos        = registry[rep_id]
        pos_label  = rep_id
        freq_label = int(round(freq * 100))

        # Skip combo if no freqk TSV exists for any of the requested sizes
        if not args.ignore_freqk:
            any_freqk = any(
                freqk_tsv_path(args.sv_type, pos_label, cov, err_lbl,
                               sz, args.n_samples, freq_label, args.k).exists()
                for sz in sizes
            )
            if not any_freqk:
                skipped += 1
                continue

        config_text = generate_config(
            sv_type=args.sv_type, coverage=cov, sv_freq=freq,
            n_samples=args.n_samples, error_rate=args.error_rate,
            k=args.k, pos=pos, pos_label=pos_label, sizes=sizes,
        )
        config_name = (f"config_var_{args.sv_type.lower()}"
                       f"_n{args.n_samples}_f{freq_label}_cov{cov}"
                       f"_err{err_lbl}_k{args.k}_{pos_label}.sh")
        config_path = config_dir / config_name
        config_path.write_text(config_text)
        config_path.chmod(0o644)

        cmd = ["sbatch", "--parsable", str(step_05b), str(config_path)]

        print(f"[{i}/{len(combos)}] {rep_id}  cov={cov}  freq={freq}  sizes={','.join(sizes)}")
        print(f"           config : {config_path}")
        print(f"           submit : {' '.join(cmd)}")

        if args.dry_run:
            print(f"           [dry-run] not submitting\n")
            continue

        result = subprocess.run(cmd, capture_output=True, text=True)
        if result.returncode != 0:
            print(f"           ERROR: {result.stderr.strip()}")
            continue
        jobid = result.stdout.strip()
        jobids.append((rep_id, cov, freq_label, jobid))
        print(f"           job id : {jobid}\n")
        submitted += 1

        if i < len(combos) and args.stagger_secs > 0:
            time.sleep(args.stagger_secs)

    if jobids:
        with open(jobids_file, "w") as f:
            f.write(f"SUBMIT_TS={ts}\n")
            for rep_id, cov, fl, jid in jobids:
                f.write(f"{rep_id}_cov{cov}_f{fl}_05b_vg_giraffe={jid}\n")
        print(f"\nJob IDs saved to {jobids_file}")

    print(f"\nSubmitted: {submitted}   Skipped (no freqk TSV): {skipped}")


if __name__ == "__main__":
    main()
